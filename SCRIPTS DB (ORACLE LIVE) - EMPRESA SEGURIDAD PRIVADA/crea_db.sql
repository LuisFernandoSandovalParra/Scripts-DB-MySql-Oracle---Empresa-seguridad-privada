-- CRETE TABLE EPS

CREATE TABLE EPS(
    ID_EPS          NUMBER(11)      NOT NULL,
    NOMBRE_EPS      VARCHAR2(60)    NOT NULL,
    TELEFONO_EPS    NUMBER(10)      NOT NULL,
    CORREO          VARCHAR2(40)    NOT NULL,
    CONSTRAINT  EPS_PK_IDE PRIMARY KEY (ID_EPS)
);


-- CRETE TABLE CURSOS

CREATE TABLE CURSOS(
    ID_CURSO            NUMBER(11)      NOT NULL,
    ESPECIALIDAD_CURSO  VARCHAR2(30)    NOT NULL,
    CONSTRAINT  CUS_PK_IDC  PRIMARY KEY (ID_CURSO)
);


-- CREATE TABLE EMPLEADOS

CREATE TABLE EMPLEADOS(
    ID_EMP                          NUMBER(11)      NOT NULL,
    NOMBRES_EMP                     VARCHAR2(60)    NOT NULL,
    APELLIDOS_EMP                   VARCHAR2(60)    NOT NULL,
    NUMERO_DOCUMENTO_EMP            NUMBER(11)      NOT NULL,
    FECHA_NACIMIENTO_EMP            DATE            NOT NULL,
    NUMERO_MOVIL_EMP                NUMBER(13)      NOT NULL,
    DIRECCION_VIVIENDA_EMP          VARCHAR2(25)    NOT NULL,
    ID_EPS                          NUMBER(11)      NOT NULL,
    ID_SUPERVISOR                   NUMBER(11)              ,
    TIPO_VIGILANTE                  CHAR(1)                 ,
    CONSTRAINT  EMP_PK_IDE  PRIMARY KEY (ID_EMP)
);


-- CRETE TABLE CURSOS_VIGILANTES

CREATE TABLE CURSOS_VIGILANTES(
    ID_EMP      	NUMBER(11)      NOT NULL,
    ID_CURSO        NUMBER(11)      NOT NULL,
    CONSTRAINT CUV_PK_IDEC  PRIMARY KEY (ID_EMP,ID_CURSO)
);


-- CRETE TABLE ESTABLECIMIENTOS

CREATE TABLE ESTABLECIMIENTOS(
    ID_EST                              NUMBER(11)      NOT NULL,
    NOMBRE_EST                          VARCHAR2(30)    NOT NULL,
    DIRECCION_EST                       VARCHAR2(25)    NOT NULL,
    TELEFONO_EST                        NUMBER(8)       NOT NULL,
    NOMBRE_ADMINISTRADOR_EST            VARCHAR2(60)    NOT NULL,
    NUMERO_DOCUMENTO_ADMINISTRADOR_EST  NUMBER(11)      NOT NULL,
    TIPO_EST                            CHAR(1)         NOT NULL,
    CONSTRAINT  EST_PK_IDE  PRIMARY KEY (ID_EST)
);


-- CRETE TABLE CONTRATOS

CREATE TABLE CONTRATOS(
    ID_CONTRATO             NUMBER(11)      NOT NULL,
    VALOR_CONTRATO          NUMBER(12)      		,
    TIPO_CONTRATO           CHAR(1)         NOT NULL,
    FECHA_INICIO_CONTRATO   DATE            NOT NULL,
    FECHA_TERMINO_CONTRATO  DATE        			,
    TIPO_CONTRATO_LABORAL   CHAR(1)                 ,
    TIPO_HORARIO            CHAR(1)                 ,
    SALARIO                 NUMBER(12)              ,
    ID_EMP          		NUMBER(11)      		,
    ID_EST                  NUMBER(11)              ,
    CONSTRAINT  CON_PK_IDC  PRIMARY KEY (ID_CONTRATO)
);


